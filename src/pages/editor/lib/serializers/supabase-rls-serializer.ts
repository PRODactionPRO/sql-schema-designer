function guessColumnType(colName: string): FieldType {
  if (colName === 'id') return 'uuid';
  if (colName.endsWith('_id')) return 'uuid';
  if (colName.endsWith('_at')) return 'timestamptz';
  if (colName.endsWith('_date')) return 'date';
  if (colName.startsWith('is_') || colName.startsWith('has_')) return 'boolean';
  if (colName === 'role' || colName === 'status' || colName === 'type') return 'text';
  if (colName === 'email') return 'varchar';
  if (colName === 'name' || colName.endsWith('_name')) return 'varchar';
  if (colName.endsWith('_url')) return 'text';
  if (colName.endsWith('_count') || colName === 'count') return 'integer';
  if (colName === 'metadata' || colName === 'config' || colName === 'settings') return 'jsonb';
  if (colName === 'amount' || colName === 'price' || colName === 'cost') return 'decimal';
  return 'text';
}

function guessReferencedTable(colName: string, tableNames: string[]): string | undefined {
  const baseName = colName.replace(/_id$/, '');

  // Direct match: brand_id → brands
  const plural = baseName + 's';
  if (tableNames.includes(plural)) return plural;
  if (tableNames.includes(baseName)) return baseName;

  // Compound names: owner_user_id → users, content_item_id → content_items
  const parts = baseName.split('_');

  // Try progressively shorter suffixes: content_item → content_items
  for (let i = 1; i < parts.length; i++) {
    const suffix = parts.slice(i).join('_');
    const suffixPlural = suffix + 's';
    if (tableNames.includes(suffixPlural)) return suffixPlural;
    if (tableNames.includes(suffix)) return suffix;
  }

  // Fuzzy match: check if any table name contains the base or vice versa
  for (const tn of tableNames) {
    const tnSingular = tn.replace(/s$/, '');
    if (tn.includes(baseName) || baseName.includes(tnSingular)) {
      return tn;
    }
  }

  return undefined;
}
