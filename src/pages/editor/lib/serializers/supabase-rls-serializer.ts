interface FkInfo {
  fromTable: string;
  fromField: string;
  toTable: string;
  toField: string;
}

function buildFkMap(schema: Schema): FkInfo[] {
  const fks: FkInfo[] = [];
  for (const rel of schema.relations) {
    const fromTable = schema.tables.find(t => t.id === rel.fromTableId);
    const toTable = schema.tables.find(t => t.id === rel.toTableId);
    const fromField = fromTable?.fields.find(f => f.id === rel.fromFieldId);
    const toField = toTable?.fields.find(f => f.id === rel.toFieldId);

    if (fromTable && toTable && fromField && toField) {
      fks.push({
        fromTable: fromTable.name,
        fromField: fromField.name,
        toTable: toTable.name,
        toField: toField.name,
      });
    }
  }
  return fks;
}

function generateHelperFunctions(schema: Schema, fks: FkInfo[]): string[] {
  const lines: string[] = [];

  const hasMembership = schema.tables.some(t =>
    t.name === 'workspace_members' || t.name.endsWith('_members')
  );

  if (hasMembership) {
    const memberTable = schema.tables.find(t =>
      t.name === 'workspace_members' || t.name.endsWith('_members')
    );
    if (memberTable) {
      const workspaceCol = memberTable.fields.find(f => f.name === 'workspace_id')?.name || 'workspace_id';
      const userCol = memberTable.fields.find(f => f.name === 'user_id')?.name || 'user_id';

      lines.push(`create or replace function public.is_workspace_member(_workspace_id uuid)`);
      lines.push(`returns boolean`);
      lines.push(`language sql`);
      lines.push(`stable`);
      lines.push(`security definer`);
      lines.push(`set search_path = public`);
      lines.push(`as $$`);
      lines.push(`  select exists (`);
      lines.push(`    select 1`);
      lines.push(`    from public.${memberTable.name} wm`);
      lines.push(`    where wm.${workspaceCol} = _workspace_id`);
      lines.push(`      and wm.${userCol} = auth.uid()`);
      lines.push(`  );`);
      lines.push(`$$;`);
      lines.push('');

      const hasRole = memberTable.fields.some(f => f.name === 'role');
      if (hasRole) {
        lines.push(`create or replace function public.has_workspace_role(_workspace_id uuid, _roles text[])`);
        lines.push(`returns boolean`);
        lines.push(`language sql`);
        lines.push(`stable`);
        lines.push(`security definer`);
        lines.push(`set search_path = public`);
        lines.push(`as $$`);
        lines.push(`  select exists (`);
        lines.push(`    select 1`);
        lines.push(`    from public.${memberTable.name} wm`);
        lines.push(`    where wm.${workspaceCol} = _workspace_id`);
        lines.push(`      and wm.${userCol} = auth.uid()`);
        lines.push(`      and wm.role = any(_roles)`);
        lines.push(`  );`);
        lines.push(`$$;`);
        lines.push('');
      }
    }
  }

  const workspaceTable = schema.tables.find(t => t.name === 'workspaces');
  if (workspaceTable) {
    const tablesWithWorkspace = schema.tables.filter(t =>
      t.fields.some(f => f.name === 'workspace_id') && t.name !== 'workspaces' && t.name !== 'workspace_members'
    );

    for (const table of tablesWithWorkspace) {
      if (table.name === 'workspaces') continue;

      const childFks = fks.filter(fk => fk.toTable === table.name);

      for (const childFk of childFks) {
        const childTable = schema.tables.find(t => t.name === childFk.fromTable);
        if (childTable && !childTable.fields.some(f => f.name === 'workspace_id')) {
          lines.push(generateWorkspaceIdForFunction(
            childTable.name,
            childFk.fromField,
            table.name
          ));
        }
      }
    }
  }

  return lines;
}

function generateWorkspaceIdForFunction(
  childTable: string,
  fkColumn: string,
  parentTable: string
): string {
  const funcName = `workspace_id_for_${childTable.replace(/s$/, '')}`;
  const paramName = `_${childTable.replace(/s$/, '')}_id`;

  return [
    `create or replace function public.${funcName}(${paramName} uuid)`,
    `returns uuid`,
    `language sql`,
    `stable`,
    `security definer`,
    `set search_path = public`,
    `as $$`,
    `  select b.workspace_id`,
    `  from public.${childTable} t`,
    `  join public.${parentTable} b on b.id = t.${fkColumn}`,
    `  where t.id = ${paramName}`,
    `  limit 1;`,
    `$$;`,
    '',
  ].join('\n');
}

function getAccessExpression(
  table: Table,
  schema: Schema,
  fks: FkInfo[]
): string {
  if (table.fields.some(f => f.name === 'workspace_id')) {
    return 'public.is_workspace_member(workspace_id)';
  }

  for (const fk of fks) {
    if (fk.fromTable === table.name) {
      const parentTable = schema.tables.find(t => t.name === fk.toTable);
      if (parentTable?.fields.some(f => f.name === 'workspace_id')) {
        const singular = table.name.replace(/s$/, '');
        return `public.is_workspace_member(public.workspace_id_for_${singular}(${fk.fromField}))`;
      }
    }
  }

  return 'true';
}

function isReferenceTable(table: Table, schema: Schema): boolean {
  const hasWorkspaceRef = table.fields.some(f =>
    f.name === 'workspace_id' || f.name === 'user_id'
  );
  const hasIncomingFks = schema.relations.some(r => r.toTableId === table.id);

  return !hasWorkspaceRef && !hasIncomingFks && table.name !== 'users';
}

function generateSelfAccessPolicies(tableName: string): string[] {
  return [
    `drop policy if exists "${tableName}_select_self" on public.${tableName};`,
    `create policy "${tableName}_select_self"`,
    `on public.${tableName}`,
    `for select`,
    `to authenticated`,
    `using (id = auth.uid());`,
    '',
    `drop policy if exists "${tableName}_update_self" on public.${tableName};`,
    `create policy "${tableName}_update_self"`,
    `on public.${tableName}`,
    `for update`,
    `to authenticated`,
    `using (id = auth.uid())`,
    `with check (id = auth.uid());`,
    '',
    `drop policy if exists "${tableName}_insert_self" on public.${tableName};`,
    `create policy "${tableName}_insert_self"`,
    `on public.${tableName}`,
    `for insert`,
    `to authenticated`,
    `with check (id = auth.uid());`,
    '',
  ];
}

function generateReadOnlyPolicies(tableName: string): string[] {
  return [
    `drop policy if exists "${tableName}_read_authenticated" on public.${tableName};`,
    `create policy "${tableName}_read_authenticated"`,
    `on public.${tableName}`,
    `for select`,
    `to authenticated`,
    `using (true);`,
    '',
  ];
}

function generateStandardPolicies(tableName: string, accessExpr: string): string[] {
  const roleExpr = accessExpr.replace('is_workspace_member', 'has_workspace_role')
    .replace(')', ", array['owner','admin','editor'])");
  const adminExpr = accessExpr.replace('is_workspace_member', 'has_workspace_role')
    .replace(')', ", array['owner','admin'])");

  return [
    `drop policy if exists "${tableName}_select_member" on public.${tableName};`,
    `create policy "${tableName}_select_member"`,
    `on public.${tableName}`,
    `for select`,
    `to authenticated`,
    `using (`,
    `  ${accessExpr}`,
    `);`,
    '',
    `drop policy if exists "${tableName}_insert_editor" on public.${tableName};`,
    `create policy "${tableName}_insert_editor"`,
    `on public.${tableName}`,
    `for insert`,
    `to authenticated`,
    `with check (`,
    `  ${roleExpr}`,
    `);`,
    '',
    `drop policy if exists "${tableName}_update_editor" on public.${tableName};`,
    `create policy "${tableName}_update_editor"`,
    `on public.${tableName}`,
    `for update`,
    `to authenticated`,
    `using (`,
    `  ${roleExpr}`,
    `)`,
    `with check (`,
    `  ${roleExpr}`,
    `);`,
    '',
    `drop policy if exists "${tableName}_delete_admin" on public.${tableName};`,
    `create policy "${tableName}_delete_admin"`,
    `on public.${tableName}`,
    `for delete`,
    `to authenticated`,
    `using (`,
    `  ${adminExpr}`,
    `);`,
    '',
  ];
}

